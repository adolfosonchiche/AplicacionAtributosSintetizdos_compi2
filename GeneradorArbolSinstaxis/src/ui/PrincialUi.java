    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        entrada = tfEntrada.getText();

        if (entrada.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debes de ingresar una expresion aritmetica...",
                    "ERROR", 2);
        } else {
            existe = false;
            StringReader reader = new StringReader(entrada);
            Lexico lexico = new Lexico(reader);
            /*pide un archivo que tenga el codigo para leerlo*/

            Sintactico parser = new Sintactico(lexico);

            try {
                parser.parse();
                
                jlbResultado.setText(parser.getResultado().toString());
                jlbNotacion.setText(parser.getPosfijo());
                existe = true;
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "A ocurrido un error irrecuperable...\n" + e,
                        "ERROR", 2);
                existe = false;
            }
            
            if (existe) {
                Runtime rt = Runtime.getRuntime();
                try {
                    //Esperamos medio segundo para dar tiempo a que la imagen se genere
                    rt.exec("nohup display graf1.jpg ");
                } catch (IOException ex) {
                    Logger.getLogger(PrincialUi.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed


